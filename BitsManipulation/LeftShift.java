public class LeftShift {
    public static void main(String[] args) {
        int x=-1;int y=9;
        // left shift by 1
        System.out.println("Left Shift of " + x + " = " + (x << 1));
        // Left shift by 2
        System.out.println("Left Shift of " + x + " = " + (x << 2));
        // left shift of y by 2
        System.out.println("Left Shift of " + y + " = " + (y << 2));
    }
}
